package com.ulsu;

public class User{
    private String name;
    private String surName;
    private String phoneNumber;
    private String email;
    private int age;

    public User(String name, String surName,String phoneNumber,String email,int age){
        this.name = name;
        this.surName = surName;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.age = age;
    }
}